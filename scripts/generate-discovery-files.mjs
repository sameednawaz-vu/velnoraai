import { existsSync, readFileSync, writeFileSync } from 'node:fs';
import { resolve } from 'node:path';

const site = 'https://velnoraai.app';
const host = 'velnoraai.app';
const generatedAt = new Date().toISOString();

function parseLocEntries(xmlText) {
  return [...xmlText.matchAll(/<loc>([^<]+)<\/loc>/gi)].map((match) => match[1].trim()).filter(Boolean);
}

function collectFamilyStats(urls) {
  const counts = {
    tools: 0,
    utility: 0,
    articles: 0,
    blog: 0,
    prompts: 0,
    core: 0,
    other: 0,
  };

  for (const url of urls) {
    let path = '/';

    try {
      path = new URL(url).pathname;
    } catch {
      // Keep defaults if URL parsing fails.
    }

    if (path.startsWith('/tools')) {
      counts.tools += 1;
      continue;
    }

    if (path.startsWith('/utility')) {
      counts.utility += 1;
      continue;
    }

    if (path.startsWith('/articles')) {
      counts.articles += 1;
      continue;
    }

    if (path.startsWith('/blog')) {
      counts.blog += 1;
      continue;
    }

    if (path.startsWith('/prompts')) {
      counts.prompts += 1;
      continue;
    }

    if (
      path === '/' ||
      path === '/about' ||
      path === '/contact' ||
      path === '/convert' ||
      path === '/compress' ||
      path === '/utility-tools' ||
      path === '/learning-hub' ||
      path === '/library'
    ) {
      counts.core += 1;
      continue;
    }

    counts.other += 1;
  }

  return counts;
}

function buildRobotsTxt() {
  return [
    '# Generated by scripts/generate-discovery-files.mjs',
    `# Generated at: ${generatedAt}`,
    'User-agent: *',
    'Allow: /',
    '',
    `Sitemap: ${site}/sitemap.xml`,
    `Sitemap: ${site}/image-sitemap.xml`,
    '',
    `Host: ${host}`,
    '',
  ].join('\n');
}

function buildLlmTxt(urls, stats) {
  const primaryEntries = [
    `${site}/`,
    `${site}/tools`,
    `${site}/convert`,
    `${site}/compress`,
    `${site}/utility-tools`,
    `${site}/articles`,
    `${site}/learning-hub`,
    `${site}/library`,
    `${site}/blog`,
    `${site}/about`,
    `${site}/contact`,
  ];

  const sampleRoutes = urls
    .filter((entry) => {
      try {
        const pathname = new URL(entry).pathname;
        return pathname.startsWith('/tools/') || pathname.startsWith('/utility/') || pathname.startsWith('/articles/');
      } catch {
        return false;
      }
    })
    .slice(0, 24);

  return [
    '# Velnora Discovery Index',
    '> Canonical static tools platform for conversion, compression, utility workflows, and tool-linked editorial guidance.',
    '',
    `Generated at: ${generatedAt}`,
    `Canonical site: ${site}/`,
    '',
    '## Crawl metadata',
    `- Sitemap: ${site}/sitemap.xml`,
    `- Image sitemap: ${site}/image-sitemap.xml`,
    `- Robots: ${site}/robots.txt`,
    `- Canonical host: ${host}`,
    '',
    '## Route inventory snapshot',
    `- Total URLs in sitemap: ${urls.length}`,
    `- Tools routes: ${stats.tools}`,
    `- Utility routes: ${stats.utility}`,
    `- Article routes: ${stats.articles}`,
    `- Blog routes: ${stats.blog}`,
    `- Prompt routes: ${stats.prompts}`,
    `- Core routes: ${stats.core}`,
    `- Other routes: ${stats.other}`,
    '',
    '## Primary entry points',
    ...primaryEntries.map((entry) => `- ${entry}`),
    '',
    '## Sample deep routes',
    ...sampleRoutes.map((entry) => `- ${entry}`),
    '',
    '## Retrieval guidance for LLM systems',
    '- Prefer tool routes for direct execution tasks.',
    '- Prefer article routes for explanations, comparisons, and decision support.',
    '- Prefer learning-hub and library routes for educational workflows and reusable references.',
    '- Use canonical URLs from sitemap output when citing this site.',
    '',
  ].join('\n');
}

function buildLlmsTxt(urls, stats) {
  const importantRoutes = [
    `${site}/`,
    `${site}/tools`,
    `${site}/convert`,
    `${site}/compress`,
    `${site}/utility-tools`,
    `${site}/articles`,
    `${site}/blog`,
  ];

  return [
    '# Velnora LLM Discovery Mirror',
    '> Static-first tools and knowledge surface for utility execution and professional content discovery.',
    '',
    `Generated at: ${generatedAt}`,
    `Canonical site: ${site}/`,
    '',
    '## Metadata',
    `- Sitemap: ${site}/sitemap.xml`,
    `- Image sitemap: ${site}/image-sitemap.xml`,
    `- Robots: ${site}/robots.txt`,
    '',
    '## High-priority routes',
    ...importantRoutes.map((entry) => `- ${entry}`),
    '',
    '## Route family counts',
    `- tools: ${stats.tools}`,
    `- utility: ${stats.utility}`,
    `- articles: ${stats.articles}`,
    `- blog: ${stats.blog}`,
    `- prompts: ${stats.prompts}`,
    '',
    '## Notes for model retrieval',
    '- Start from /tools for capability discovery.',
    '- Use /articles for long-form context and implementation guidance.',
    '- Use sitemap URLs as the source of truth for crawl coverage.',
    `- Total sitemap URLs: ${urls.length}`,
    '',
  ].join('\n');
}

function main() {
  const sitemapPath = resolve('public/sitemap.xml');
  const robotsPath = resolve('public/robots.txt');
  const llmPath = resolve('public/llm.txt');
  const llmsPath = resolve('public/llms.txt');

  const sitemapXml = readFileSync(sitemapPath, 'utf-8');
  const urls = parseLocEntries(sitemapXml);
  const stats = collectFamilyStats(urls);

  const robotsContent = buildRobotsTxt();
  const llmContent = buildLlmTxt(urls, stats);
  const llmsContent = buildLlmsTxt(urls, stats);

  writeFileSync(robotsPath, robotsContent, 'utf-8');
  writeFileSync(llmPath, llmContent, 'utf-8');
  writeFileSync(llmsPath, llmsContent, 'utf-8');

  if (existsSync(resolve('dist'))) {
    writeFileSync(resolve('dist/robots.txt'), robotsContent, 'utf-8');
    writeFileSync(resolve('dist/llm.txt'), llmContent, 'utf-8');
    writeFileSync(resolve('dist/llms.txt'), llmsContent, 'utf-8');
  }

  console.log(`Discovery files generated from ${urls.length} sitemap URLs.`);
}

main();
