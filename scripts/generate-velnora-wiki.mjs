import { appendFileSync, existsSync, mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';

const TOOLS_PATH = resolve('src/content/data/tools.json');
const SURFACES_PATH = resolve('src/content/data/freeconvert-catalog.json');
const COMPETITOR_PATH = resolve('src/content/data/competitor-convertio-formats.json');

const OUTPUT_WIKI_PATH = resolve('docs/velnora-wiki.md');
const OUTPUT_INDEX_PATH = resolve('knowledge/llm-wiki/wiki-index.json');
const OUTPUT_SCHEMA_PATH = resolve('knowledge/llm-wiki/wiki-record.schema.json');
const OUTPUT_LOG_PATH = resolve('knowledge/llm-wiki/wiki-log.jsonl');

const toolsData = readJson(TOOLS_PATH);
const surfacesData = readJson(SURFACES_PATH);
const competitorData = existsSync(COMPETITOR_PATH) ? readJson(COMPETITOR_PATH) : null;

const generatedAt = new Date().toISOString();
const categorySnapshots = buildCategorySnapshots(toolsData.categories, toolsData.tools);
const surfaceSnapshots = buildSurfaceSnapshots(surfacesData.surfaces);

const wikiIndex = {
  generatedAt,
  model: 'karpathy-llm-wiki-pattern',
  sources: [
    { id: 'tools', file: 'src/content/data/tools.json', description: 'Master catalog for professional tools.' },
    { id: 'surfaces', file: 'src/content/data/freeconvert-catalog.json', description: 'Convert/Compress/Tools surface catalog.' },
    competitorData
      ? {
          id: 'convertio-formats',
          file: 'src/content/data/competitor-convertio-formats.json',
          description: 'Competitor format intelligence feed generated by scraper.',
        }
      : null,
  ].filter(Boolean),
  stats: {
    categories: categorySnapshots.length,
    tools: toolsData.tools.length,
    publishedTools: toolsData.tools.filter((tool) => tool.status === 'published').length,
    surfaces: surfaceSnapshots.length,
    surfaceGroups: surfaceSnapshots.reduce((sum, surface) => sum + surface.groupCount, 0),
    surfaceTools: surfaceSnapshots.reduce((sum, surface) => sum + surface.toolCount, 0),
    competitorCategories: competitorData?.totals?.categories ?? 0,
    competitorFormats: competitorData?.totals?.formats ?? 0,
  },
  categories: categorySnapshots.map((category) => ({
    slug: category.slug,
    name: category.name,
    order: category.order,
    description: category.description,
    toolCount: category.toolCount,
    publishedCount: category.publishedCount,
  })),
  surfaces: surfaceSnapshots,
  contracts: {
    wikiMarkdownFile: 'docs/velnora-wiki.md',
    wikiIndexFile: 'knowledge/llm-wiki/wiki-index.json',
    wikiSchemaFile: 'knowledge/llm-wiki/wiki-record.schema.json',
    wikiLogFile: 'knowledge/llm-wiki/wiki-log.jsonl',
  },
};

const wikiMarkdown = buildWikiMarkdown({
  generatedAt,
  categorySnapshots,
  surfaceSnapshots,
  competitorData,
});

const schema = {
  $schema: 'https://json-schema.org/draft/2020-12/schema',
  title: 'VelnoraWikiIndex',
  type: 'object',
  required: ['generatedAt', 'model', 'sources', 'stats', 'categories', 'surfaces', 'contracts'],
  properties: {
    generatedAt: { type: 'string' },
    model: { type: 'string' },
    sources: {
      type: 'array',
      items: {
        type: 'object',
        required: ['id', 'file', 'description'],
        properties: {
          id: { type: 'string' },
          file: { type: 'string' },
          description: { type: 'string' },
        },
      },
    },
    stats: {
      type: 'object',
      required: ['categories', 'tools', 'publishedTools', 'surfaces', 'surfaceGroups', 'surfaceTools'],
      properties: {
        categories: { type: 'number' },
        tools: { type: 'number' },
        publishedTools: { type: 'number' },
        surfaces: { type: 'number' },
        surfaceGroups: { type: 'number' },
        surfaceTools: { type: 'number' },
      },
    },
    categories: { type: 'array' },
    surfaces: { type: 'array' },
    contracts: { type: 'object' },
  },
};

writeFile(OUTPUT_WIKI_PATH, `${wikiMarkdown}\n`);
writeFile(OUTPUT_INDEX_PATH, `${JSON.stringify(wikiIndex, null, 2)}\n`);
writeFile(OUTPUT_SCHEMA_PATH, `${JSON.stringify(schema, null, 2)}\n`);
appendLog(OUTPUT_LOG_PATH, {
  timestamp: generatedAt,
  event: 'wiki-generated',
  stats: wikiIndex.stats,
});

console.log('Velnora wiki generated successfully.');
console.log(`- Markdown: ${OUTPUT_WIKI_PATH}`);
console.log(`- Index JSON: ${OUTPUT_INDEX_PATH}`);
console.log(`- Schema: ${OUTPUT_SCHEMA_PATH}`);
console.log(`- Log: ${OUTPUT_LOG_PATH}`);

function readJson(filePath) {
  return JSON.parse(readFileSync(filePath, 'utf8'));
}

function writeFile(filePath, content) {
  mkdirSync(dirname(filePath), { recursive: true });
  writeFileSync(filePath, content, 'utf8');
}

function appendLog(filePath, payload) {
  mkdirSync(dirname(filePath), { recursive: true });
  appendFileSync(filePath, `${JSON.stringify(payload)}\n`, 'utf8');
}

function buildCategorySnapshots(categories, tools) {
  const grouped = new Map();

  for (const category of categories) {
    grouped.set(category.slug, {
      slug: category.slug,
      name: category.name,
      description: category.description,
      order: category.order,
      tools: [],
    });
  }

  for (const tool of tools) {
    const bucket = grouped.get(tool.category);
    if (bucket) {
      bucket.tools.push(tool);
    }
  }

  return Array.from(grouped.values())
    .map((category) => {
      const sortedTools = [...category.tools].sort((first, second) => first.name.localeCompare(second.name));
      const publishedCount = sortedTools.filter((tool) => tool.status === 'published').length;
      return {
        ...category,
        tools: sortedTools,
        toolCount: sortedTools.length,
        publishedCount,
      };
    })
    .sort((first, second) => first.order - second.order);
}

function buildSurfaceSnapshots(surfaces) {
  return surfaces.map((surface) => {
    const groupSnapshots = surface.groups.map((group) => ({
      slug: group.slug,
      name: group.name,
      toolCount: group.tools.length,
      tools: [...group.tools].sort((first, second) => first.name.localeCompare(second.name)),
    }));

    return {
      slug: surface.slug,
      name: surface.name,
      description: surface.description,
      groupCount: groupSnapshots.length,
      toolCount: groupSnapshots.reduce((sum, group) => sum + group.toolCount, 0),
      groups: groupSnapshots,
    };
  });
}

function buildWikiMarkdown({ generatedAt, categorySnapshots, surfaceSnapshots, competitorData }) {
  const totalTools = categorySnapshots.reduce((sum, category) => sum + category.toolCount, 0);
  const publishedTools = categorySnapshots.reduce((sum, category) => sum + category.publishedCount, 0);

  const lines = [];

  lines.push('# Velnora Internal Wiki');
  lines.push('');
  lines.push('Canonical internal snapshot for tools, utility surfaces, and competitor format intelligence.');
  lines.push('');
  lines.push(`- Generated: ${generatedAt}`);
  lines.push(`- Categories: ${categorySnapshots.length}`);
  lines.push(`- Tools: ${totalTools}`);
  lines.push(`- Published tools: ${publishedTools}`);
  lines.push(`- Utility surfaces: ${surfaceSnapshots.length}`);
  lines.push('');

  lines.push('## 1. Site Description');
  lines.push('');
  lines.push('Velnora is a static, browser-first workspace that ships deterministic utilities across professional workflows and file operations.');
  lines.push('Tool execution remains client-side compatible for Astro and GitHub Pages deployment constraints.');
  lines.push('');

  lines.push('## 2. Category Index');
  lines.push('');
  lines.push('| Order | Category | Slug | Tools | Published | Description |');
  lines.push('| ---: | --- | --- | ---: | ---: | --- |');
  for (const category of categorySnapshots) {
    lines.push(
      `| ${category.order} | ${escapeMarkdown(category.name)} | ${category.slug} | ${category.toolCount} | ${category.publishedCount} | ${escapeMarkdown(category.description)} |`
    );
  }

  for (const category of categorySnapshots) {
    lines.push('');
    lines.push(`### ${escapeMarkdown(category.name)}`);
    lines.push('');
    lines.push(`- Slug: ${category.slug}`);
    lines.push(`- Description: ${escapeMarkdown(category.description)}`);
    lines.push(`- Tools in category: ${category.toolCount}`);
    lines.push('');
    lines.push('| ID | Tool | Slug | Mode | Engine | Status |');
    lines.push('| ---: | --- | --- | --- | --- | --- |');

    for (const tool of category.tools) {
      lines.push(
        `| ${tool.id} | ${escapeMarkdown(tool.name)} | ${tool.slug} | ${tool.mode} | ${tool.engine} | ${tool.status} |`
      );
    }
  }

  lines.push('');
  lines.push('## 3. Utility Surface Atlas');
  lines.push('');
  lines.push('| Surface | Slug | Groups | Tools | Description |');
  lines.push('| --- | --- | ---: | ---: | --- |');

  for (const surface of surfaceSnapshots) {
    lines.push(
      `| ${escapeMarkdown(surface.name)} | ${surface.slug} | ${surface.groupCount} | ${surface.toolCount} | ${escapeMarkdown(surface.description)} |`
    );
  }

  for (const surface of surfaceSnapshots) {
    lines.push('');
    lines.push(`### ${escapeMarkdown(surface.name)} Surface`);
    lines.push('');
    lines.push('| Group | Slug | Tools |');
    lines.push('| --- | --- | ---: |');

    for (const group of surface.groups) {
      lines.push(`| ${escapeMarkdown(group.name)} | ${group.slug} | ${group.toolCount} |`);
    }

    lines.push('');
    lines.push('| Tool | Slug | Status | Group |');
    lines.push('| --- | --- | --- | --- |');

    for (const group of surface.groups) {
      for (const tool of group.tools) {
        lines.push(`| ${escapeMarkdown(tool.name)} | ${tool.slug} | ${tool.status} | ${escapeMarkdown(group.name)} |`);
      }
    }
  }

  lines.push('');
  lines.push('## 4. Competitor Intelligence Snapshot');
  lines.push('');

  if (competitorData) {
    lines.push(`- Source: ${competitorData.source.url}`);
    lines.push(`- Snapshot generated: ${competitorData.generatedAt}`);
    lines.push(`- Categories tracked: ${competitorData.totals.categories}`);
    lines.push(`- Formats tracked: ${competitorData.totals.formats}`);
    lines.push('');
    lines.push('| Category | Formats | Read | Write |');
    lines.push('| --- | ---: | ---: | ---: |');

    for (const category of competitorData.categories) {
      lines.push(`| ${escapeMarkdown(category.name)} | ${category.formatCount} | ${category.readCount} | ${category.writeCount} |`);
    }
  } else {
    lines.push('Competitor dataset is not available yet. Run `npm run scrape:convertio` and regenerate this wiki.');
  }

  lines.push('');
  lines.push('## 5. LLM Wiki Operating Model (Karpathy Pattern)');
  lines.push('');
  lines.push('The internal memory model follows a deterministic loop inspired by the Karpathy LLM Wiki gist:');
  lines.push('');
  lines.push('1. Raw sources are gathered into structured JSON files.');
  lines.push('2. A generated wiki index consolidates source metadata and totals.');
  lines.push('3. A JSON schema validates the wiki-index contract.');
  lines.push('4. A JSONL log records every regeneration event for auditability.');
  lines.push('5. Public-facing docs consume generated index files instead of hand-edited fragments.');
  lines.push('');

  lines.push('## 6. Regeneration Commands');
  lines.push('');
  lines.push('- `npm run scrape:convertio`');
  lines.push('- `npm run generate:wiki`');
  lines.push('- `npm run refresh:intel`');

  return lines.join('\n');
}

function escapeMarkdown(value) {
  return String(value ?? '').replace(/\|/g, '\\|');
}
